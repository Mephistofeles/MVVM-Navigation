namespace Bezysoftware.Navigation
{
    /// <summary>
    /// Identifies a type of navigation, can be forward or backward.
    /// </summary>
    public enum NavigationType
    {
        /// <summary>
        /// Navigating forward to new page.
        /// </summary>
        Forward,

        /// <summary>
        /// Navigating backward to previous page.
        /// </summary>
        Backward
    }
}
